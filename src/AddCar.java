    public void addCar()
    {
        try
        {
            String s="insert into car values(?,?,?,?,?,?,?,?)";
            pst=conn.prepareCall(s);
            pst.setString(1,jTextField1.getText());
            pst.setString(2,(String) jComboBox1.getSelectedItem());
            pst.setString(3,(String) jComboBox2.getSelectedItem());
            pst.setInt(4,Integer.parseInt((String)jComboBox3.getSelectedItem()));
            pst.setString(5,jTextField2.getText());
            pst.setString(6,"no");
            pst.setString(7,null);
            pst.setInt(8,0);
            pst.execute();
            JOptionPane.showMessageDialog(null,"New Car Created");
            pst.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }
